function UI:Window(title,HC)
	local TS=game:GetService('TweenService')
	local Info=TweenInfo.new(0.2,Enum.EasingStyle.Linear,Enum.EasingDirection.Out,0,false,0)
	local Window={}
	local ui=GetUI()
	local btns={}
	for _, v in pairs(ui.Frame.TabHolder:GetChildren()) do if(v:IsA('TextButton'))then table.insert(btns,v) end end
	for _, v in pairs(btns) do
		v.Text=v.Name
		v.MouseButton1Up:Connect(function()
			for _, v2 in pairs(btns) do
				if(v2~=v)then
					TS:Create(v2.BtnFocused,Info,{Size=UDim2.new(0,0,0,1),BackgroundTransparency=1}):Play()
				end
			end
			TS:Create(v.BtnFocused,Info,{Size=UDim2.new(0,90,0,1),BackgroundTransparency=0}):Play()
		end)
	end
	local function Drag()
		local tween = game:GetService('TweenService')
		local tweeninfo = TweenInfo.new(0.09,Enum.EasingStyle.Linear,Enum.EasingDirection.Out,0,false,0)
		local frame=ui.Frame
		local dragging = false
		local dragInput, mousePos, framePos
		local input=game:GetService('UserInputService')
		
		frame.InputBegan:Connect(function(input)
			if input.UserInputType == Enum.UserInputType.MouseButton1 then
				dragging = true
				mousePos = input.Position
				framePos = ui.Frame.Position
		
				input.Changed:Connect(function()
					if input.UserInputState == Enum.UserInputState.End then
						dragging = false
					end
				end)
			end
		end)
		
		frame.InputChanged:Connect(function(input)
			if input.UserInputType == Enum.UserInputType.MouseMovement then
				dragInput = input
			end
		end)
		
		input.InputChanged:Connect(function(input)
			if input == dragInput and dragging then
				local delta = input.Position - mousePos
				tween:Create(ui.Frame,tweeninfo,{Position=UDim2.new(framePos.X.Scale, framePos.X.Offset + delta.X, framePos.Y.Scale, framePos.Y.Offset + delta.Y)}):Play()
			end
		end)
		local pos=ui.Frame.TabHolder.Position
		game:GetService('RunService').RenderStepped:Connect(function()
			ui.Frame.TabHolder.Position=pos
		end)
	end
	local temps=ui.Frame.Templates
	ui.Frame.TextLabel.Text=title
	local function Close()
		for _, v in pairs(ui.Frame:GetDescendants()) do
			if(v.Parent~=ui.Frame)and(v.Name~='TextLabel')or(v.Name~='Close')or(v.Name~='Frame')then
				pcall(function()
					spawn(function()
						for x=0,1,0.2 do
							v.BackgroundTransparency=x
							task.wait()
						end
					end)
				end)
			end
		end
		repeat task.wait() until ui.Frame.Test.BackgroundTransparency==1
		for _, v in pairs(ui.Frame:GetDescendants()) do
			if(v.Parent~=ui.Frame)and(v.Name~='TextLabel')or(v.Name~='Close')or(v.Name~='Frame')then
				v:Destroy()
			end
		end
		game:GetService('TweenService'):Create(ui.Frame,TweenInfo.new(0.6,Enum.EasingStyle.Linear,Enum.EasingDirection.Out,0,false,0),{Size=UDim2.new(0,460,0,18)})
		task.wait(0.66)
		for _, v in pairs(ui.Frame:GetDescendants()) do
			pcall(function()
				spawn(function()
					for x=0,1,0.2 do
						v.BackgroundTransparency=x
						task.wait()
					end
				end)
			end)
		end
		repeat task.wait() until ui.Frame.Frame.BackgroundTransparency==1
		for _, v in pairs(ui.Frame:GetDescendants()) do
			v:Destroy()
		end
		game:GetService('TweenService'):Create(ui.Frame,TweenInfo.new(0.6,Enum.EasingStyle.Linear,Enum.EasingDirection.Out,0,false,0),{Size=UDim2.new(0,0,0,18)})
		task.wait(0.66)
		ui.Frame:Destroy()
	end
	ui.Close.MouseButton1Up:Connect(Close)
