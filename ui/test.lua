								option.BackgroundTransparency=x
								option.TextTransparency=x
							end
						end
						task.wait()
					end
				end
			end
			for _, option in pairs(args.Options) do
				local new=optiontemp:Clone()
				new.Text=option
				new.Name=option
				new.BackgroundTransparency=1
				new.MouseButton1Up:Connect(function()
					openclose()
