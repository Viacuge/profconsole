		function Tab:Slider(args)
			args.Callback=args.Callback or function() end
			args.Text=args.Text or 'Error'
			args.Icon=args.Icon or 'rbxassetid://7734058803'
			args.Max=args.Max or 10
			args.Min=args.Min or 0
			args.Increment=args.Increment or 0
			args.Value=args.Value or 'Error'
			local temp=temps.Slider.Frame4:Clone()
